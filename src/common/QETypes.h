#ifndef QETYPES_H
#define QETYPES_H

typedef signed char         QE_CHAR;
typedef unsigned char       QE_UCHAR;
typedef signed short        QE_SHORT;
typedef unsigned short      QE_USHORT;
typedef signed int          QE_INT;
typedef unsigned int        QE_UINT;
typedef signed long         QE_LONG;
typedef unsigned long       QE_ULONG;
typedef signed long long    QE_LONG_LONG;
typedef unsigned long long  QE_ULONG_LONG;
typedef float               QE_FLOAT;
typedef double              QE_DOUBLE;
typedef long double         QE_LDOUBLE;

#endif