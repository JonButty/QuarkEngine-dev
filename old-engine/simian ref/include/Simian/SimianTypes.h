#ifndef SIMIAN_SIMIANTYPES_H_
#define SIMIAN_SIMIANTYPES_H_

namespace Simian
{
    // signed integrals
    typedef char                s8;
    typedef short               s16;
    typedef int                 s32;
    typedef long long           s64;

    // unsigned integrals
    typedef unsigned char       u8;
    typedef unsigned short      u16;
    typedef unsigned int        u32;
    typedef unsigned long long  u64;

    // floating points
    typedef float               f32;
    typedef double              f64;
}

#endif
